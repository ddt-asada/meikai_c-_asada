#include <iostream>

using namespace std;

int main()
{
	//キーボードから整数値を読み込むための型。
	int x;

	//int型の整数値の入力を促す。
	cout << "整数値：";
	//変数xに代入させる。
	cin >> x;

	//キーボードより入力されたxを表示し、正常に処理できているか確認する。
	cout << x << "と入力しましたね。\n";
}
